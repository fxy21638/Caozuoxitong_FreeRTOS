#ifndef __TOUCH_H
#define __TOUCH_H

#include "stm32f4xx.h"
#include <stdint.h>

/* ========================================================================== */
/*  touch.h — 触摸屏驱动接口 (GT9157 / FT5x06, I2C3)                            */
/* ========================================================================== */

/* 触摸控制器 I2C 地址 */
#define GT9157_I2C_ADDR     0x14        /* GT9157 默认 7-bit I2C 地址          */
#define FT5X06_I2C_ADDR     0x38        /* FT5x06 默认 7-bit I2C 地址          */

/* ========================================================================== */
/*  公开接口                                                                     */
/* ========================================================================== */
uint8_t  Touch_Init(void);              /* 初始化触摸控制器, 返回 1=成功        */
uint8_t  Touch_Scan(void);              /* 扫描触摸, 返回 1=检测到按下的点       */
uint16_t Touch_GetX(void);              /* 获取按下点 X 坐标 (0~799)            */
uint16_t Touch_GetY(void);              /* 获取按下点 Y 坐标 (0~479)            */

#endif /* __TOUCH_H */
